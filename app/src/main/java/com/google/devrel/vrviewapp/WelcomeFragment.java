    class MakeRequestTask extends AsyncTask<Void, Void, List<String>> { // background getting stuff
        private com.google.api.services.slides.v1.Slides mService = null;

        MakeRequestTask(GoogleAccountCredential credential) {
            HttpTransport transport = AndroidHttp.newCompatibleTransport();
            JsonFactory jsonFactory = JacksonFactory.getDefaultInstance();
            mService = new com.google.api.services.slides.v1.Slides.Builder(
                    transport, jsonFactory, credential)
                    .setApplicationName("VRViewApp")
                    .build();
            testMessage = "Done";
            doneBackgroundTask = true;
        }

        protected List<String> doInBackground(Void... params) {
            //testMessage = "DONE";
            try {
                Presentation response = mService.presentations().get(presentationID).execute();
                List<Page> slides = response.getSlides();

                testMessage = slides.size() + " slides";

                // getting slide ID's
                for (int i = 0; i < slides.size(); i++) {
                    Page slide = slides.get(i);
                    slideIDs.add(slide.getObjectId());
                }
                doneBackgroundTask = true;
                return slideIDs;

            } catch (IOException e) {
                testMessage = "ERRORRRORRR";
                mLastError = e;
                cancel(true);
                doneBackgroundTask = true;
                return null;
            }
        }
    }

